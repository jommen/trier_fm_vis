package de.hochschuleTrier.fmv.exceptions;

public class ComplexConstraintParseException extends Exception {

	public ComplexConstraintParseException() {
		super();
	}

	public ComplexConstraintParseException(final String message, final Throwable cause) {
		super(message, cause);
	}

	public ComplexConstraintParseException(final String message) {
		super(message);
	}

	public ComplexConstraintParseException(final Throwable cause) {
		super(cause);
	}

}
